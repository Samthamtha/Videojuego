def get_text(key, idioma="Español"):
    """
    Obtiene el texto traducido según el idioma.
    
    Args:
        key: Clave del texto a traducir
        idioma: Idioma deseado ("Español" o "Inglés")
    
    Returns:
        Texto traducido o la clave si no se encuentra
    """
    if idioma not in TRANSLATIONS:
        idioma = "Español"  # Fallback a español
    
    return TRANSLATIONS[idioma].get(key, key)

